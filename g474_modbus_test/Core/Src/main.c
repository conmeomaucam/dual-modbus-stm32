    {
      last_test_time = HAL_GetTick();
      char test_msg[] = "G4_Loopback_OK!";
      
      // Kiểm tra và tự động xóa các lỗi ngoại vi UART (Overrun, Frame Error, Noise)
      if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_ORE) != RESET) {
        printf("[Loopback] Overrun Error (ORE) detected! Clearing...\r\n");
        __HAL_UART_CLEAR_FLAG(&huart1, UART_CLEAR_OREF);
      }
      if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_FE) != RESET) {
        printf("[Loopback] Framing Error (FE) detected! Clearing...\r\n");
        __HAL_UART_CLEAR_FLAG(&huart1, UART_CLEAR_FEF);
      }
      if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_NE) != RESET) {
        printf("[Loopback] Noise Error (NE) detected! Clearing...\r\n");
        __HAL_UART_CLEAR_FLAG(&huart1, UART_CLEAR_NEF);
      }

      uint32_t dma_cnt_before = __HAL_DMA_GET_COUNTER(huart1.hdmarx);
      printf("[Loopback] Transmitting: %s (DMA NDTR before: %d)\r\n", test_msg, (int)dma_cnt_before);
      
      // Gửi chuỗi qua USART1_TX (PC4)
      HAL_UART_Transmit(&huart1, (uint8_t *)test_msg, sizeof(test_msg), 100);
      
      // Chờ 150ms để DMA và ngắt IDLE nhận hết dữ liệu về PC5
      HAL_Delay(150);
      
      uint32_t dma_cnt_after = __HAL_DMA_GET_COUNTER(huart1.hdmarx);
      printf("[Loopback] DMA NDTR after: %d\r\n", (int)dma_cnt_after);
      
      // Kiểm tra xem có dữ liệu trong ring buffer nhận được không
      uint32_t rx_len = rt_ringbuffer_data_len(&usart1_rb);
      if (rx_len > 0)
      {
        uint8_t rx_buf[64] = {0};
        rt_ringbuffer_get(&usart1_rb, rx_buf, rx_len);
        printf("[Loopback] Success! Received: %s\r\n\r\n", rx_buf);
      }
      else
      {
        printf("[Loopback] Failed! No data received. (Please check if PC4 is shorted to PC5)\r\n\r\n");
      }
    }
#else
    Modbus_Process(); // Vẫn giữ Modbus chạy ngầm
#endif
    /* USER CODE END WHILE */

